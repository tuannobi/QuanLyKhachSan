    private void xuLyDangNhap() {
        loadTaiKhoan();
        for (TaiKhoanDTO taiKhoanDTO:listTaiKhoan){ //chạy list tài khoản đã load
            taiKhoan=new TaiKhoanDTO();
            if(jtfTenDangNhap.getText().equals(taiKhoanDTO.getTenDangNhap())){
                if (String.valueOf(jpMatKhau.getPassword()).equals(taiKhoanDTO.getMatKhau())){
                    taiKhoan.setMaTaiKhoan(taiKhoanDTO.getMaTaiKhoan());
                    taiKhoan.setTenDangNhap(taiKhoanDTO.getTenDangNhap());
                    taiKhoan.setMatKhau(taiKhoanDTO.getMatKhau());
                    taiKhoan.setMaNhanVien(taiKhoanDTO.getMaNhanVien());
                    if (taiKhoanDTO.getMaLoaiTaiKhoan()==1){
                        taiKhoan.setMaLoaiTaiKhoan(taiKhoanDTO.getMaLoaiTaiKhoan());
                        ManHinhMainJFrame_GiamDoc giamDoc=new ManHinhMainJFrame_GiamDoc();
                        giamDoc.showWindow();
                        hiddenWindow();
                        break;
                    }
                    else if (taiKhoanDTO.getMaLoaiTaiKhoan()==2){
                        taiKhoan.setMaLoaiTaiKhoan(taiKhoanDTO.getMaLoaiTaiKhoan());
                        ManHinhMainJFrame_LeTan leTan =new ManHinhMainJFrame_LeTan();
                        leTan.showWindow();
                        hiddenWindow();
                        break;
                    }
                    
                }
            }
        }
        if (taiKhoan.getMaLoaiTaiKhoan()<=0){
            JOptionPane.showMessageDialog(null, "Không tìm thấy tài khoản này");
        }
    }
    private void loadTaiKhoan(){
        listTaiKhoan=new ArrayList<>();
        listTaiKhoan=TaiKhoanBUS.layDuLieuTaiKhoan();
        
    }

