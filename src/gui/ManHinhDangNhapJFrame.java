    private void chinhSuaKichThuocIcon(String name,JLabel lbLabel){
        String input="/image/"+name+"";
        ImageIcon icon=new ImageIcon(Toolkit.getDefaultToolkit().getImage(getClass().getResource(input)));
        Image img =icon.getImage();
        Image imggImage=img.getScaledInstance(lbLabel.getWidth(),lbLabel.getHeight(),Image.SCALE_SMOOTH);
        ImageIcon i=new ImageIcon(imggImage);
        lbLabel.setIcon(i);
    }
    
