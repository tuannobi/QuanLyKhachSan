    private boolean kiemTraTinhDayDu() {
       if (jtfTenDangNhap.getText().equals("") && String.valueOf(jpMatKhau.getPassword()).equals("")){
            JOptionPane.showMessageDialog(null, "Vui lòng nhập tên đăng nhập và mật khẩu");
            return false;
        }
        else if (String.valueOf(jpMatKhau.getPassword()).equals("")){
            JOptionPane.showMessageDialog(null, "Vui lòng nhập mật khẩu");
            return false;
        }
        else if ( jtfTenDangNhap.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Vui lòng nhập tên đăng nhập");
            return false;
        }
        else{
            return true;
        }
    }
