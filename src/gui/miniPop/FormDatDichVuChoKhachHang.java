    private void hienThiDanhSachDichVuCoTheDatDuoc()
{
        dtmBang1=new DefaultTableModel();
        listDichVu=DichVuBus.layDanhSachDichVuCoTheDatDuoc();
        dtmBang1=(DefaultTableModel)jtbDanhSachDichVu.getModel();
     
    for(DichVu dv:listDichVu)
    {
        Vector<Object> vt=new Vector<Object>();
        vt.add(dv.getMaDichVu());
        vt.add(dv.getTenDichVu());
        vt.add(dv.getGiaTien());
        dtmBang1.addRow(vt);
    }    
    jtbDanhSachDichVu.setModel(dtmBang1);
}

    private void themDichVuVaoDanhSach() {
        String soLuong=JOptionPane.showInputDialog(null, "Nhập số lượng\n");
       dtmBang2=new DefaultTableModel();
       dtmBang2=(DefaultTableModel)jtbDichVuDat.getModel();
       Vector<Object> vt=new Vector<>();
       vt.add(selectedTable1.getMaDichVu());
       vt.add(selectedTable1.getTenDichVu());
       vt.add(selectedTable1.getGiaTien());
        try {
            vt.add(Integer.parseInt(soLuong));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Nhập sai định dạng số");
            return;
        }
      dtmBang2.addRow(vt);
      jtbDichVuDat.setModel(dtmBang2);
      
      //thêm vào list
      ChiTietDichVuDTO hoaDonDichVuDTO=new ChiTietDichVuDTO(selectedTable1.getMaDichVu(),selectedJPanelPhong.getMaHoaDon(),Integer.parseInt(soLuong));
      listHoaDonDichVu.add(hoaDonDichVuDTO);
    }

    private void xuLyXoaDichVuTuDanhSach() {
        listHoaDonDichVu.remove(selectedTable2);
        dtmBang2.removeRow(selectedRowCanXoa);
    }

    private boolean yeuCauXuLyThemDichVuChoKhachHang() {
        return ChiTietDichVuBUS.luuThongTinDatDichVu(listHoaDonDichVu);
    }

    private void luuThongTinDichVuKhiClickCanThem() {
        selectedTable1=new DichVu();
        int selectedRow=jtbDanhSachDichVu.getSelectedRow();
        if (selectedRow==-1)
        return;
        selectedTable1=listDichVu.get(selectedRow);
    }

    private void luuThongTinDichVuKhiClickCanXoa() {
        selectedTable2=new ChiTietDichVuDTO();
        selectedRowCanXoa=jtbDichVuDat.getSelectedRow();
        if (selectedRowCanXoa==-1)
        return;
        selectedTable2=listHoaDonDichVu.get(selectedRowCanXoa);
    }

    private void xuLyThemDichVuVaoDanhSach() {
       if (selectedTable1!=null)
        themDichVuVaoDanhSach();
        else
            JOptionPane.showMessageDialog(null, "Chọn dịch vụ cần thêm"); 
    }
