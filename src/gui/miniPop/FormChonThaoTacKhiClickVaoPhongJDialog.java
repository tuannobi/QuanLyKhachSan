    private void yeuCauXuLyInHoaDon() {
         HoaDonBus.inHoaDon(selectedJPanelPhong.getMaHoaDon());
 }
    
    private boolean yeuCauXuLyThanhToanHoaDon() {
       boolean check=HoaDonBus.thanhToanHoaDon(selectedJPanelPhong.getMaHoaDon());
       if (check){
           JOptionPane.showMessageDialog(null, "Thanh toán thành công");
           return true;
       }
       else{
           JOptionPane.showMessageDialog(null, "Thanh toán thất bại");
           return false;
       }
    }

