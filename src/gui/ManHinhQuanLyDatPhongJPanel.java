    public void yeuCauHienThiListPhongDuocDat(){
        jpnHienThi.removeAll();      
        listPhongJPanels=PhongBUS.layDanhSachCacPhongCoTheDat(jcTuNgay.getDate(),jcDenNgay.getDate(),((LoaiPhongDTO)jcbbLoaiPhong.getSelectedItem()).getMaLoaiPhong());
        if(listPhongJPanels.size()<=0){
            JOptionPane.showMessageDialog(null, "Không có phòng nào phù hợp");
            return;
        }
        jpnHienThi.setLayout(new FlowLayout(FlowLayout.LEFT));
        for (JPanelPhong phong:listPhongJPanels){
            jpnHienThi.add(phong);
        }
        jpnHienThi.repaint();
        jpnHienThi.validate();
    }
    
    private void hienThiLoaiPhong(){
        listLoaiPhongDTOs=LoaiPhongBUS.layDuLieuLoaiPhong();
        for (LoaiPhongDTO loaiPhongDTO:listLoaiPhongDTOs){
            jcbbLoaiPhong.addItem(loaiPhongDTO);
        }
    }

    private boolean kiemTraNgayThangHopLe() {
        Date date=new Date();
       if (jcTuNgay.getDate()==null && jcDenNgay.getDate()==null){
            JOptionPane.showMessageDialog(null, "Vui lòng chọn ngày đi và ngày đến");
            return false;
        }
        else if(jcTuNgay.getDate()==null){
            JOptionPane.showMessageDialog(null, "Vui lòng chọn ngày đến");
            return false;
        }
        else if(jcDenNgay.getDate()==null){
            JOptionPane.showMessageDialog(null, "Vui lòng chọn ngày đi");
            return false;
        }
        else if(jcTuNgay.getDate().getTime()<=date.getTime() || jcDenNgay.getDate().getTime()<=date.getTime()){
            JOptionPane.showMessageDialog(null, "Chọn ngày lớn hơn ngày hiện tại");
            return false;
        }
        else if (jcTuNgay.getDate().getTime()>=jcDenNgay.getDate().getTime()){
            JOptionPane.showMessageDialog(null, "Ngày đi phải lớn hơn ngày đến");
            return false;
        }
        else{
        return true;
        } 
    }
